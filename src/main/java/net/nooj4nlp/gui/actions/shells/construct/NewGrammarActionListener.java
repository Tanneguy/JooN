package net.nooj4nlp.gui.actions.shells.construct;

import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;

import javax.swing.JDialog;

import net.nooj4nlp.gui.dialogs.GrammarDialog;

/**
 * 
 * ActionListener that opens a new grammar editor shell
 * 
 */
public class NewGrammarActionListener implements ActionListener
{

	@Override
	public void actionPerformed(ActionEvent arg0)
	{
		GrammarDialog dialog = new GrammarDialog();
		dialog.setModal(true);
		dialog.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
		dialog.setVisible(true);
	}

}
