package net.nooj4nlp.controller.packageconfigurationdialog;

import java.io.Serializable;

public class WindowPosition implements Serializable
{
	private static final long serialVersionUID = 7573632402456703052L;

	private int posX, posY;
	private int width, height;

	public WindowPosition(int posX, int posY, int width, int height)
	{
		this.posX = posX;
		this.posY = posY;
		this.width = width;
		this.height = height;
	}

	public int getPosX()
	{
		return posX;
	}

	public int getPosY()
	{
		return posY;
	}

	public int getWidth()
	{
		return width;
	}

	public int getHeight()
	{
		return height;
	}
}