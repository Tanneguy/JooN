package net.nooj4nlp.controller.SyntacticTreeShell;

import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;

public class OptionsActionListner implements ActionListener {

	private SyntacticTreeShellController controller;
	
	public OptionsActionListner(SyntacticTreeShellController controller) {
		this.controller = controller;
	}

	@Override
	public void actionPerformed(ActionEvent arg0) {
		controller.getShell().repaint();
	}

}
